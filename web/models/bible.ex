  defenum TestamentEnum, :testament, [:new, :old]

  defenum VersionEnum, :version, [:asv, :niv, :kjv, :nkjv, :nlt]

  schema "bibles" do
    field :chapter, :integer
    field :verse, :integer
    field :content, :string
  end

  @doc """
  Builds a changeset based on the `struct` and `params`.
  """
  def changeset(struct, params \\ %{}) do
    struct
    |> Ecto.Changeset.cast(params, [:chapter, :verse, :content])
    |> EctoEnum.cast(params, [:version, :testament, :book])
    |> validate_required([:chapter, :verse, :content])
  end
end
